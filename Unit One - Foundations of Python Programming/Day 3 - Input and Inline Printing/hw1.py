number = int(input("Pick a number: "))
z = int(input("Pick your second number: "))
sum = number + z 
quotient = number/z
subtract = number-z
product = number*z

print(f"{number} + {z} = {sum}.")
print(f"{number}/{z} = {quotient}")
print(f"{number} - {z} = {subtract}")
print(f"{number} x {z} = {product}")